n = int(input('Digite um número inteiro: '))
dobro = n * 2
triplo = n * 3
raiz = n ** (1/2)
print('O valor {} tem como dobro {}; seu triplo é {}; sua raiz quadrada é {:.2f}'.format(n, dobro, triplo, raiz))
